package day02;

public class Customer {
	String id;
	String name;
	int age;
	String sex;
	String phone;
	String addr;
	public Customer(String id, String name, int age, String sex, String phone,
			String addr) {
		super();
		this.id = id;
		this.name = name;
		this.age = age;
		this.sex = sex;
		this.phone = phone;
		this.addr = addr;
	}	

}
